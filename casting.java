public class casting {
    public static void main(String[] args) {
        //inplicit casting: smaller datatype is casted into larger datatype
        double a=109.5;
        double final_a=a+18;
        System.out.println(a);
        System.out.println(final_a);

        //explicit casting: larger datatype is casted into smaller datatype
        int b=(int)198.8;
        System.out.println(b);
    }
    
}
